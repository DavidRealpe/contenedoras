package model;

public class Contenedora{

	private double ancho;
	private double alto;
	private double largo;

	public Contenedora(double ancho, double alto, double largo){
		this.ancho = ancho;
		this.alto = alto;
		this.largo = largo;
	}

	public double getAncho(){
		return ancho;
	}

	public double getAlto(){
		return alto;
	}

	public double getLargo(){
		return largo;
	}


}
